                       0.5  1   1   0.25 2;
                       0.5  1   1   0.5 2;
                       2    2   2   1   2;
                       0.25 0.5 0.5 0.5 1
                       ];
eigenvector1 = (relasiAntarKriteria*relasiAntarKriteria);
eigenvector2 = eigenvector1(:,1)+eigenvector1(:,2)+eigenvector1(:,3)+eigenvector1(:,4)+eigenvector1(:,5);
eigenvector3 = eigenvector2(1,1)+eigenvector2(2,1)+eigenvector2(3,1)+eigenvector2(4,1)+eigenvector2(5,1);
eigenvector4 = eigenvector2/eigenvector3;
datah = dataa*eigenvector4;

p=[1];
for i = 2:size(datah, 1)
    k=[i];
    p=[p;k];
end
datao=[p datah];
    
set(myform.uitable2,'data',datao);



