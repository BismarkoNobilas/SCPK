maksSemester = 10;
myform = guidata(gcbo);
dataa = get(handles.uitable1, 'data');
dataa(:,1) = dataa(:,1)/maksIPK;
dataa(:,2) = dataa(:,2)/maksPenghasilan;
dataa(:,3) = dataa(:,3)/maksTanggungan;
